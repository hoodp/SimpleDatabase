package package1;

/**********************************************************************
 * The following interface contains methods that are used in the 
 * SimpleDatabase class. 
 * @author Paul Hood
 * @version 12/04/2013
 *********************************************************************/
interface iSimpleDatabase {
	/******************************************************************
	 * Method is used to insert a student into the database
	 *****************************************************************/
	void insert(Student student);

	/******************************************************************
	 * Used to delete a student from the DB by the G Number
	 *****************************************************************/
	boolean delete(String gNumber);

	/******************************************************************
	 * Returns string of the entire DB
	 *****************************************************************/
	String toString();

	/******************************************************************
	 * finds a student, otherwise returns null
	 *****************************************************************/
	Student find(String gNumber);

	/******************************************************************
	 * Reverses the database
	 *****************************************************************/
	void reverseList();

	/******************************************************************
	 * removes duplicates from the database
	 *****************************************************************/
	void removeDuplicates();

	/******************************************************************
	 * Undo the previous command
	 *****************************************************************/
	void undo();

	/******************************************************************
	 * Load files from DB
	 *****************************************************************/
	void loadDB(String fileName);

	/******************************************************************
	 * Save files from DB
	 *****************************************************************/
	void saveDB(String fileName);
}
